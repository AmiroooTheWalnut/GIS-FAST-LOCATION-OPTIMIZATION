    private void colorSampleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_colorSampleMouseClicked
        // TODO add your handling code here:
        if (imageController != null) {
            imageController.isEditNeeded=true;
            imageController.isPickColor = true;
            imageController.pickColorIndex = myOrder;
            imageController.setCursor();
            Robot bot;
            try {
                bot = new Robot();
                bot.mouseMove(imageController.imagePloter1.getLocationOnScreen().x + imageController.imagePloter1.getWidth() / 2, imageController.imagePloter1.getLocationOnScreen().y + imageController.imagePloter1.getHeight() / 2);
            } catch (AWTException ex) {
                Logger.getLogger(ColorDefinition.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_colorSampleMouseClicked

    private void deleteColorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteColorActionPerformed
        // TODO add your handling code here:
        if (imageController != null) {
            imageController.colorDefinitions.remove(myOrder);
            imageController.refreshList();
            imageController.isEditNeeded=true;
        }
    }//GEN-LAST:event_deleteColorActionPerformed
